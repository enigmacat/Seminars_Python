# Задача №2 Дан список, вывести отдельно буквы и цифры, пользуясь filter.
# =========================================================================
ls = [28, 'trkh', 8720, 'pain']
print(list(filter(lambda x: isinstance(x, (str)), ls)), list(
    filter(lambda x: isinstance(x, (int, float)), ls)))
